import math

# the above code imports everything from python math
# you can print number by just putting them in a print statement
print(2)
print(3.1415)
print(-6)

# all types of numbers work and you have the basic math operators too!
print(3*4)
print(4/2)

# % is the remainder operator will give you the remainder or division
print(10 % 3)

# you can also use "()" to change order of operations
print(3*(4+5))

my_num = 64
print(str(my_num) + " my favorite number!")
# you can use functions on nubmers too!

my_num = -64

# gives you the absolute value of a number
print(abs(my_num))

# you can use pow(num1,num2) will let you do exponents
print(pow(5, 2))

# functions such as min(num1, num2) or max(num1, num2) will give you the 
# minimum or maximum value
# when you import everything you have more functions like floor(num) and
# ceil(num) which truncates the decimal or rounds up regardless the number
# respectively
print(math.pi)
